/**
 * Simple class for testing and integration purposes.
 */
public class MyCalculator {

    public int sum (int a, int b) {
        return a+b;
    }

    public int subtract (int a, int b) {
        return a-b;
    }

    public double divide (int a, int b) {
        return ((double)a)/b;
    }

    public int multilply (int a, int b) {
        return a*b;
    }


}
